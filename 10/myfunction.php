}

/*	max_num($x,$y,$z)--------------------------------3
	Обчислення найбільшого серед трьох чисел. 
	$a,$b,$c-задані числа.
*/
function max_num($x,$y,$z){
$max=$x;
if($y>$max){
$max=$y;
}
if($z<$max){
$max=$z;
}
return $max;
}


