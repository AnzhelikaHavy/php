/*	area_sqr($x)-------------------------------------1
	Обчислити площу квадрата. 
	$x-довжина сторони квадрата.
*/
function area_sqr($x){
$y=pow($x,2);
return $y;
}

/*	area_circle($x)----------------------------------2
