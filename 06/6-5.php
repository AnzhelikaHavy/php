<?
/*	6.5 Створіть програму знаходження cуми чисел:   
		1/1 + 1/2 + 1/3 + 1/4 + ... + 1/20

ПЕРЕВІРКА: 	Відповідь: 3,5977
*/

echo "<H2>--------------------------- Цикл for ----------------------------</H2>";

$sum=0;
for($i=1; $i<=20; $i++){
$sum=$sum+1/$i;
}
echo"Відповідь:".$sum;

echo "<H2>-------------------------- Цикл while ---------------------------</H2>";

$i=1;
$sum=0;
while($i<=20){
$sum+=1/$i;
$i++;
}
echo"Відповідь:".round($sum,2);

?>

